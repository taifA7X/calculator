function add(a, b) {
    let result = a + b;
    return result
}

function subtract(a, b) {
    let result = a - b;
    return result
}

function multiply(a, b) {
    let result = a * b;
    return result
}

function divide(a, b) {
    result = a / b;
    return result
}

function operate(operator, a, b) {
    result = operator(a, b);
    return result
}

